    private void btnGuardarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarVentaActionPerformed
            try {
                
        Connection conn = SesionUsuario.conexion;
        conn.setAutoCommit(false);
        // Obtener valores desde los campos
        String vin = txtVin.getText().trim();
        int idCliente = Integer.parseInt(txtIdCliente.getText().trim());

        // Obtener y convertir fecha
        String fechaTexto = txtFecha.getText().trim();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        java.util.Date fechaUtil = sdf.parse(fechaTexto);
        java.sql.Date fechaSQL = new java.sql.Date(fechaUtil.getTime());
        

        // Obtener tipo de pago desde JComboBox
        
     String seleccionado = SeleccionPago.getSelectedItem().toString().trim().toLowerCase();
        String tipoPago;
        if (seleccionado.equals("contado")) {
            tipoPago = "contado";
        } else if (seleccionado.equals("credito")) {
            tipoPago = "credito";
        } else {
            JOptionPane.showMessageDialog(null, "Tipo de pago inválido");
            return;
        }
        // Validar cuotas según tipo de pago
        int cuotas;
        if (tipoPago.equals("contado")) {
            cuotas = 0;
        } else {
            // El campo debe estar habilitado en este punto
            cuotas = Integer.parseInt(txtCuotas.getText().trim());
            if (cuotas != 6 && cuotas != 10 && cuotas != 12 && cuotas != 24) {
                JOptionPane.showMessageDialog(null, "Para crédito, las cuotas deben ser 6, 10, 12 o 24.");
                return;
            }
        }

        // Llamada al procedimiento PL/SQL
        CallableStatement cs = conn.prepareCall("{ call proyectobd.registrar_venta(?, ?, ?, ?, ?, ?) }");

        cs.setString(1, vin);
        cs.setInt(2, idCliente);
        cs.setDate(3, fechaSQL);
        cs.setInt(4, cuotas);
        cs.setString(5, tipoPago);
        cs.registerOutParameter(6, java.sql.Types.INTEGER); // id_venta (OUT)

        cs.execute();
        conn.commit();

        int idVenta = cs.getInt(6);
        JOptionPane.showMessageDialog(null, "Venta registrada exitosamente. ID de venta: " + idVenta);
        txtIdVenta.setText(String.valueOf(idVenta)); // Si tienes un campo para mostrarlo

        cs.close();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Verifica que los campos numéricos estén bien ingresados.");
    } catch (ParseException e) {
        JOptionPane.showMessageDialog(null, "La fecha debe tener el formato dd/MM/yyyy.");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al registrar la venta: " + e.getMessage());
        e.printStackTrace();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Ocurrió un error: " + e.getMessage());
        e.printStackTrace();
    }


    }//GEN-LAST:event_btnGuardarVentaActionPerformed

