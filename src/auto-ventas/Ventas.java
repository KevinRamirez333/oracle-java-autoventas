    private void btnConsultarDPIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarDPIActionPerformed
         try {
        Connection conn = SesionUsuario.conexion;

        CallableStatement cs = conn.prepareCall("{ call proyectobd.obtener_cliente_por_dpi(?, ?, ?) }");

        String dpi = txtDPI.getText().trim();
        cs.setString(1, dpi); // Entrada: DPI

        cs.registerOutParameter(2, java.sql.Types.INTEGER); // Salida: ID Cliente
        cs.registerOutParameter(3, java.sql.Types.VARCHAR); // Salida: Nombre

        cs.execute();

        int idCliente = cs.getInt(2);
        String nombre = cs.getString(3);

        if (cs.wasNull() || nombre == null) {
            JOptionPane.showMessageDialog(null, "No se encontró un cliente con ese DPI.");
        } else {
            txtIdCliente.setText(String.valueOf(idCliente));
            txtNombre.setText(nombre);
        }

        cs.close();

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al buscar cliente: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnConsultarDPIActionPerformed

    private void btnConsultarPorVinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarPorVinActionPerformed
        try {
        Connection conn = SesionUsuario.conexion;

        CallableStatement cs = conn.prepareCall("{ call proyectobd.obtener_vehiculo_por_vin(?, ?, ?) }");

        String vin = txtVin.getText().trim();
        cs.setString(1, vin); // Entrada: VIN

        cs.registerOutParameter(2, java.sql.Types.VARCHAR); // Salida: Modelo
        cs.registerOutParameter(3, java.sql.Types.NUMERIC); // Salida: Precio de venta

        cs.execute();

        String modelo = cs.getString(2);
        double precioVenta = cs.getDouble(3);

        if (modelo == null) {
            JOptionPane.showMessageDialog(null, "No se encontró un vehículo con ese VIN.");
        } else {
            DecimalFormat df = new DecimalFormat("#,##0.00");
            txtModelo.setText(modelo);
            txtPrecioVenta.setText(df.format(precioVenta));
        }

        cs.close();

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al consultar vehículo: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnConsultarPorVinActionPerformed

