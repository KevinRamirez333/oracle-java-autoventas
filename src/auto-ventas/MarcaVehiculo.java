    private void btnCrearNuevoIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearNuevoIDActionPerformed
        try {
               Connection conn = SesionUsuario.conexion;
               CallableStatement cstmt = conn.prepareCall("{? = call proyectobd.obtener_nuevo_id_marca()}");
               cstmt.registerOutParameter(1, Types.INTEGER);
               cstmt.execute();
               int nuevoId = cstmt.getInt(1);
               txtIdMarca.setText(String.valueOf(nuevoId));
               txtIdMarca.setEditable(false);
               txtNombreMarca.setText("");
               txtNombreMarca.requestFocus();
           } catch (SQLException e) {
               e.printStackTrace();
               JOptionPane.showMessageDialog(this, "Error al generar ID de marca.");
        }
    }//GEN-LAST:event_btnCrearNuevoIDActionPerformed

    private void btnConsultarMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarMarcaActionPerformed
        try {
            int idMarca = Integer.parseInt(txtIdMarca.getText().trim());

            Connection conn = SesionUsuario.conexion;
            CallableStatement cstmt = conn.prepareCall("{ ? = call proyectobd.obtener_nombre_marca(?) }");

            cstmt.registerOutParameter(1, Types.VARCHAR); // el valor que devuelve la función
            cstmt.setInt(2, idMarca);

            cstmt.execute();

            String nombre = cstmt.getString(1); // el resultado está en el primer parámetro
            txtNombreMarca.setText(nombre);
            txtNombreMarca.setEditable(true); // puedes decidir si lo dejas editable o no

            JOptionPane.showMessageDialog(this, "Consulta completada.");

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al consultar la marca: " + ex.getMessage());
        }

    }//GEN-LAST:event_btnConsultarMarcaActionPerformed

    private void btnActualizarMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarMarcaActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "¿Seguro que desea modificar el nombre referente al ID marca?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
               int idMarca = Integer.parseInt(txtIdMarca.getText().trim());
               String nuevoNombre = txtNombreMarca.getText().trim();

               Connection conn = SesionUsuario.conexion;
               CallableStatement cstmt = conn.prepareCall("{call proyectobd.actualizar_marca(?, ?)}");
               cstmt.setInt(1, idMarca);
               cstmt.setString(2, nuevoNombre);
               cstmt.execute();

               JOptionPane.showMessageDialog(this, "Marca actualizada correctamente.");
           } catch (SQLException ex) {
               ex.printStackTrace();
               JOptionPane.showMessageDialog(this, "Error al actualizar la marca.");
           }
       }
    }//GEN-LAST:event_btnActualizarMarcaActionPerformed

    private void btnEliminarMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarMarcaActionPerformed
    int confirm = JOptionPane.showConfirmDialog(this, "¿Seguro que desea eliminar esta marca?", "Confirmar", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            int idMarca = Integer.parseInt(txtIdMarca.getText().trim());

            Connection conn = SesionUsuario.conexion;
            CallableStatement cstmt = conn.prepareCall("{call proyectobd.eliminar_marca(?)}");
            cstmt.setInt(1, idMarca);
            cstmt.execute();

            JOptionPane.showMessageDialog(this, "Marca eliminada correctamente.");
            txtIdMarca.setText("");
            txtNombreMarca.setText("");

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al eliminar la marca.");
        }
    }
    }//GEN-LAST:event_btnEliminarMarcaActionPerformed

