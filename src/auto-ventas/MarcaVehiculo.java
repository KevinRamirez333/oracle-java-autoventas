    private void btnCrearMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearMarcaActionPerformed
       
       String nombreMarca = txtNombreMarca.getText().trim();

       if (nombreMarca.isEmpty()) {
           JOptionPane.showMessageDialog(this, "Debe ingresar el nombre de la marca.");
           return;
       }

       try {
           Connection conn = SesionUsuario.conexion;
           CallableStatement cstmt = conn.prepareCall("{call proyectobd.insertar_marca_vehiculo(?)}");

           
           cstmt.setString(1, nombreMarca);
           cstmt.execute();

           JOptionPane.showMessageDialog(this, "Marca insertada correctamente.");
           // Limpieza y recarga
       } catch (SQLException ex) {
           ex.printStackTrace();
           JOptionPane.showMessageDialog(this, "Error al insertar marca:\n" + ex.getMessage());
       }
       txtIdMarca.setEditable(true);
    }//GEN-LAST:event_btnCrearMarcaActionPerformed

